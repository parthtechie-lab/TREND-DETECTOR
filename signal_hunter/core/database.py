# ---------------------------------------------------------------------------
# Engine & session factory
# ---------------------------------------------------------------------------

engine = create_async_engine(
    settings.DATABASE_URL,
    echo=False,
    pool_pre_ping=True,
    future=True,
)

AsyncSessionLocal: async_sessionmaker[AsyncSession] = async_sessionmaker(
    bind=engine,
    expire_on_commit=False,
    class_=AsyncSession,
)


# ---------------------------------------------------------------------------
# Session context manager
# ---------------------------------------------------------------------------

@asynccontextmanager
async def get_session():  # type: ignore[return]
    """Yield an ``AsyncSession`` and automatically close it afterwards.

    Usage::

        async with get_session() as session:
            result = await session.execute(select(RawItem))
    """
    async with AsyncSessionLocal() as session:
        try:
            yield session
        except Exception:
            await session.rollback()
            raise
        finally:
            await session.close()


# ---------------------------------------------------------------------------
# Schema initialisation
# ---------------------------------------------------------------------------

async def init_db() -> None:
    """Create all tables that do not yet exist.

    Safe to call on every startup — SQLAlchemy uses ``CREATE TABLE IF NOT
    EXISTS`` semantics under the hood via ``checkfirst=True``.
    """
    async with engine.begin() as conn:
        await conn.run_sync(Base.metadata.create_all)
